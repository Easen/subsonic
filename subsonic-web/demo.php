Try the online demo to get a taste of what Subsonic is all about!

The Subsonic demo server is running on a 1 TB Ripserver NAS device. The Ripserver is a Linux-based ripping,
storage and streaming system.

Please take notice of the following:

o All the music in the demo is free, and courtesy of xxx.
o Not all Subsonic's features are available in the demo version. For instance, program settings can not be viewed
  or changed. Please refer to the screenshots to see what you're missing.
o The demo server has limited bandwidth, so the audio may stutter if there are many concurrent users.
o The demo server might be unavailable from time to time. After all, it's located in the basement of my apartment :-)
        
